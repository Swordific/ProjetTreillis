public class Point {
    private double px;
    private double py;

    public Point(double px, double py) {
        this.px = px;
        this.py = py;
    }
        
    public double getPx() {
        return px;
    }

    public double getPy() {
        return py;
    }

    public void setPx(double px) {
        this.px = px;
    }

    public void setPy(double py) {
        this.py = py;
    }
    
   
    
     

    
}
